var a1 =document.querySelector('#a1')

a1.addEventListener("click", function(){
 if( this.style.fontSize=='10px'){
  this.style.fontSize='20px'
  this.style.width='90%'
  this.style.backgroundColor=' rgba(56, 46, 46,0.7)'
  
 }else{
    this.style.fontSize='10px'
  this.style.width='60%'
  this.style.backgroundColor=' rgba(5,7,12,0.7)'
 }
});

